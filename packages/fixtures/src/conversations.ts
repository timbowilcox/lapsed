const today = new Date("2026-05-13T08:00:00Z");
function tsMinutesAgo(min: number): string {
  return new Date(today.getTime() - min * 60_000).toISOString();
}
function tsHoursAgo(h: number): string {
  return new Date(today.getTime() - h * 60 * 60_000).toISOString();
}
function tsDaysAgo(d: number): string {
  return new Date(today.getTime() - d * 24 * 60 * 60_000).toISOString();
}

